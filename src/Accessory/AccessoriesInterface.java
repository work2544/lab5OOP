package Accessory;

/**
 * An interface for Accessory objects
 * 
 */
public interface AccessoriesInterface {
    /**
     * 
     * @return durable of Acessory
     */
    int durable();

    /**
     * 
     * @return weight of Acessory
     */
    int weight();

    /**
     * 
     * @return damage of Acessory
     */
    int damage();

    /**
     * 
     * @return defense of Acessory
     */
    int defense();

}
